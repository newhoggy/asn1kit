package org.asn1gen.runtime.java;

public class Tuple5<A, B, C, D, E> {
  public final A a;
  public final B b;
  public final C c;
  public final D d;
  public final E e;
  
  public Tuple5(final A a, final B b, final C c, final D d, final E e) {
    this.a = a;
    this.b = b;
    this.c = c;
    this.d = d;
    this.e = e;
  }
}