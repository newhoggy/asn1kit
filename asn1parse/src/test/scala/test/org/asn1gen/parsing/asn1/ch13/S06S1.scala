package test.org.asn1gen.parsing.asn1.ch13 {
  class TestS06S1 extends TestCase {
    
    object TheParser extends Asn1Parser {
      def parse[N](root: Parser[N], input: String) =
        phrase(root)(new lexical.Scanner(input))
    }
    
    import TheParser._
    
    @Test def test_1() {
      val text = """
        Morse ::= PrintableString (FROM ("."|"-"|" "))
        IDCardNumber ::= NumericString (FROM ("0".."9"))
        PushButtonDialSequence ::= IA5String (FROM ("0".."9"|"*"|"#"))
      """
      parse(assignmentList, text) match {
        case Success(_, _) => ()
        case x => fail("Parse failure: " + x)
      }
    }
    
    @Test def test_2() {
      val text = """
        MorseAlphabet ::= PrintableString ("."|"-"|" ")
      """
      parse(assignmentList, text) match {
        case Success(_, _) => ()
        case x => fail("Parse failure: " + x)
      }
    }
    
    @Test def test_3() {
      val text = """
        WrongType ::= IA5String (FROM ("Albert".."Zoe"))
      """
      parse(assignmentList, text) match {
        case Success(_, _) => ()
        case x => fail("Parse failure: " + x)
      }
    }
    
    @Test def test_4() {
      val text = """
        Dna ::= PrintableString (FROM ("TAGC"))
      """
      parse(assignmentList, text) match {
        case Success(_, _) => ()
        case x => fail("Parse failure: " + x)
      }
    }
    
    @Test def test_5() {
      val text = """
        Dna::= PrintableString (FROM ("T"|"A"|"G"|"C"))
      """
      parse(assignmentList, text) match {
        case Success(_, _) => ()
        case x => fail("Parse failure: " + x)
      }
    }
  }
}
